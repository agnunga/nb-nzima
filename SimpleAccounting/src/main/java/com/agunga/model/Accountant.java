package com.agunga.model;

import java.io.Serializable;
import javax.persistence.Entity;
import javax.persistence.GeneratedValue;
import javax.persistence.Id;
import javax.persistence.NamedQueries;
import javax.persistence.NamedQuery;
import javax.persistence.Table;

/**
 *
 * @author agunga
 */
@NamedQueries(
        @NamedQuery(name = "findByUsernameAndPassword", query = "SELECT at FROM Accountant at WHERE at.username = :username AND at.password =:password")
)
@Entity
@Table(name = "accountants")
public class Accountant implements Serializable {

    @Id
    @GeneratedValue
    private long id;
    private String name;
    private String username;
    private String password;

    public long getId() {
        return id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public void setId(long id) {
        this.id = id;
    }

    public String getUsername() {
        return username;
    }

    public void setUsername(String username) {
        this.username = username;
    }

    public String getPassword() {
        return password;
    }

    public void setPassword(String password) {
        this.password = password;
    }

}
